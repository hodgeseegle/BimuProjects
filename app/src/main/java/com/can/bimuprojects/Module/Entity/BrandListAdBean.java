public class BrandListAdBean {
    private String id;
    private String image;
    private String name;
    private String product;
    private String money;
    private String brand_has_followed;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getProduct() {
        return product;
    }

    public void setProduct(String product) {
        this.product = product;
    }

    public String getMoney() {
        return money;
    }

    public void setMoney(String money) {
        this.money = money;
    }

    public String getBrand_has_followed() {
        return brand_has_followed;
    }

    public void setBrand_has_followed(String brand_has_followed) {
        this.brand_has_followed = brand_has_followed;
    }
}
